# Numerical types
# Integers and Floats, complex numbers, big ints

# Integers
# Full numbers
print(10)
print(type(10))

# floats
# decimal numbers
print(10.0)
print(type(10.0))

# They can be used together
print(10/3)

# Add
print(3 + 4)

# Subtract
print(3 - 4)

# Divide
print(10 / 2)

# Multiply
print(10 * 2)

# Something with this thing % (Modulus)
# this gives us the remainder
print(10%2)
print(10%3)


# Casting data types
# From string to integer
example_var = int('10')
print(type(example_var))